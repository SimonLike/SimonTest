@interface SimonTableViewController ()
@property(strong,nonatomic)NSArray *simonArr;

@end

@implementation SimonTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.simonArr=@[@"one",@"two",@"three",@"four"];
 
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.simonArr.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    static NSString *CustomCellIdentifier = @"CustomCellIdentifier";

    SimonTableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:CustomCellIdentifier];
    
    if (cell==nil) {
        cell= [[[NSBundle mainBundle]loadNibNamed:@"SimonTableViewCell" owner:nil options:nil] firstObject];
    }
    cell.textLabel.text=[self.simonArr objectAtIndex:indexPath.row];
    
    return cell;
}


